//2_ALGORITMO SalarioLiquido
var vHAula, vtrabMes, per_descINSS, sLiq, sBrut, descINSS;
vHAula = parseFloat(prompt("Insira o valor da Hora Aula"));
vtrabMes = parseFloat(prompt("Insira o número de horas trabalhadas no mês"));
per_descINSS = parseFloat(prompt("Insira o percentual de desconto do INSS"));
